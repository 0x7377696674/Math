// Determinants!

// Number Fields
//
// By number field, we mean any set K of objects, which, after using any of the four operations
// (addition, subtraction, division, multiplication) give elements of K.
//
// These operations have the following properties(field axioms).
//
// Addition
// -Commutative -> a + b = b + a;
// -Associative -> (a + b) + c = a + (b + c);
// -Negative -> there's a number for every a in K that a + y = 0;
// -Identity -> There is a number 0 (zero) in K such that 0 + a = a for every a in K;
//
// Multiplication
// Commutative -> a * b = b * a;
// Associative -> (a * b) * c = a * (b * c);
// Identity -> for every a != 0 in K there's a number that a * y = 1;
//
// Multiplication is distributive over Addition
// a(b + c) = ab + ac;

